    private void jLabelPorcentajeTipoHabMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelPorcentajeTipoHabMouseReleased
        this.jLabelPorcentajeTipoHab.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 255, 255)));
        this.removeAll();
        String query = "Select * from habitacion where estado = 1";
        this.conn.Consult(query);
        int n = 0;
        try {
            this.conn.rs.last();//se posiciona en el ultimo elemento de la tabla
            n = this.conn.rs.getRow();//regresa el numero actual del registro
            this.conn.rs.first(); //se posiciona en el primer registro de la tabla
        } catch (Exception e) {
            System.out.println(" Error #1");
        }
        int tipo1 = 0, tipo2 = 0, tipo3 = 0, aux;
        for (int i = 0; i < n; i++) {
            try {
                aux = this.conn.rs.getInt("tipo");
                if (aux == 1) {
                    tipo1 += 1;
                }
                if (aux == 2) {
                    tipo2 += 1;
                }
                if (aux == 3) {
                    tipo3 += 1;
                }
                this.conn.rs.next();
            } catch (SQLException ex) {
                Logger.getLogger(JPanelConsultas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        Datos.clear();
        Datos.addValue(tipo1, "Ocupadas", "Sencillas");
        Datos.addValue(tipo2, "Ocupadas", "Dobles");
        Datos.addValue(tipo3, "Ocupadas", "Suite");
        Datos.addValue(12 - tipo1, "Disponibles", "Sencillas");
        Datos.addValue(10 - tipo2, "Disponibles", "Dobles");
        Datos.addValue(8 - tipo3, "Disponibles", "Suite");
        grafica = ChartFactory.createBarChart(
                "Estado de habitaciones", //titulo
                "Tipo", //Etiqueta horizontal
                "Cantidad", //etiqueta o nombre vertical
                Datos, //modelo de datos
                PlotOrientation.VERTICAL, // orientacion
                true, // etiquetas indicativas
                true, // etiqueta mouse
                false // urls (sitios web)
        );
        this.jPanelGraficaPorcentajeOcupacion.removeAll();
        this.jPanelGraficaPorcentajeOcupacion.setVisible(false);
        ChartPanel Panel = new ChartPanel(grafica);
        grafica.setBackgroundPaint(Color.white);
        this.jTextArea1.setText("\nSencillas: " + (tipo1 * 100) / 12 + "% ocupadas\n"
                + "\n"
                + "Dobles: " + (tipo2 * 100) / 10 + "% ocupadas\n"
                + "\n"
                + "Suite: " + (tipo3 * 100) / 8 + "% ocupadas");
        this.jPanelGraficaPorcentajeOcupacion.setLayout(new BorderLayout());
        Panel.setDomainZoomable(true);
        this.jPanelGraficaPorcentajeOcupacion.add(Panel, BorderLayout.CENTER);
        this.jPanelGraficaPorcentajeOcupacion.setVisible(true);

        this.add(this.JPanelPorcentajesXTipo);
        this.repaint();
        this.revalidate();
    }//GEN-LAST:event_jLabelPorcentajeTipoHabMouseReleased

