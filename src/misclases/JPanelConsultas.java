    private void generarTablaHuesped() {
        String query = "select * from registro ORDER BY nomCliente ASC";
        this.conn.Consult(query);
        int n = 0;
        try {
            this.conn.rs.last();//se posiciona en el ultimo elemento de la tabla
            n = this.conn.rs.getRow();//regresa el numero actual del registro
            this.conn.rs.first(); //se posiciona en el primer registro de la tabla
        } catch (Exception e) {
            System.out.println(" Error #1");
        }
        if (n != 0) {
            Object datos[][] = new Object[n][4];
            DateFormat formateadorFechaCompleta = DateFormat.getDateInstance(DateFormat.MEDIUM);
            int auxTipo;
            for (int i = 0; i < n; i++) {
                try {
                    datos[i][3] = formateadorFechaCompleta.format(this.conn.rs.getDate("fechaOut"));
                    datos[i][0] = this.conn.rs.getString("nomCliente");
                    datos[i][1] = this.conn.rs.getInt("numHabitacion");
                    auxTipo = this.conn.rs.getInt("tipoHabitacion");
                    if (auxTipo == 1) {
                        datos[i][2] = "Sencilla";
                    }
                    if (auxTipo == 2) {
                        datos[i][2] = "Doble";
                    }
                    if (auxTipo == 3) {
                        datos[i][2] = "Triple";
                    }

                    this.conn.rs.next(); //avanza al siguiente registro
                } catch (Exception e) {
                    System.out.println("Error 2");
                    e.printStackTrace();
                }
            }
            String columnas[] = {"Nombre", "Habitación", "Tipo", "Fecha salida"};
            this.jTable1.getTableHeader().setFont(new java.awt.Font("Decker", 1, 18));
            this.jTable1.getTableHeader().setOpaque(false);
            this.jTable1.getTableHeader().setBackground(Color.black);
            this.jTable1.getTableHeader().setForeground(Color.white);
            this.jTable1.getTableHeader().setAlignmentX(CENTER_ALIGNMENT);
            this.jTable1.setModel(new DefaultTableModel(datos, columnas) {
                @Override
                public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return false;
                }
            });
            this.jTable1.setRowHeight(25);
        }//fin if
        else {
            JOptionPane.showMessageDialog(null, "No hay datos");
        }
    }

    private void buscarHabitacionesDisponibles() {
        this.jLabelHabitacionesSuite.setText("");
        this.jLabelHabitacionesDobles.setText("");
        this.jLabelHabitacionesSencillas.setText("");
        String query = "select * from habitacion where piso =  ' " + (this.jComboBoxPiso.getSelectedIndex() + 1) + "' AND estado = 0 ORDER BY numero";
        this.conn.Consult(query);
        int n = 0;
        try {
            this.conn.rs.last();//se posiciona en el ultimo elemento de la tabla
            n = this.conn.rs.getRow();//regresa el numero actual del registro
            this.conn.rs.first(); //se posiciona en el primer registro de la tabla
        } catch (Exception e) {
            System.out.println(" Error #1");
        }
        if (n != 0) {
            int numHab = 0;
            int tipo = 0;
            String dispSen = "", dispDoble = "", dispSuite = "";
            for (int i = 0; i < n; i++) {
                try {
                    numHab = this.conn.rs.getInt("numero");
                    tipo = this.conn.rs.getInt("tipo");
                    if (tipo == 1) {
                        if (dispSen.isEmpty()) {
                            dispSen = "" + numHab;
                        } else {
                            dispSen += ", " + numHab;
                        }
                    }
                    if (tipo == 2) {
                        if (dispDoble.isEmpty()) {
                            dispDoble = "" + numHab;
                        } else {
                            dispDoble += ", " + numHab;
                        }
                    }
                    if (tipo == 3) {
                        if (dispSuite.isEmpty()) {
                            dispSuite = "" + numHab;
                        } else {
                            dispSuite += ", " + numHab;
                        }
                    }
                    this.conn.rs.next(); //avanza al siguiente registro
                } catch (Exception e) {
                    System.out.println("Error 2");
                }
            }
            this.jLabelHabitacionesSencillas.setText(dispSen);
            this.jLabelHabitacionesDobles.setText(dispDoble);
            this.jLabelHabitacionesSuite.setText(dispSuite);
        } else {
            JOptionPane.showMessageDialog(null, "No hay habitaciones disponibles", "Hotel Lleno", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void consultaPorNum() {
        this.jCheckBoxDosExtraConf1.setSelected(false);
        this.jCheckBoxExtraConf1.setSelected(false);
        try {
            String query = "SELECT * FROM `registro` WHERE numHabitacion = '" + this.jTextFieldNNumHab1.getText().trim() + "'";
            this.conn.Consult(query);
            if (this.conn.rs.getRow() == 0) {
                this.jPanelInfoHuespedByNum.setVisible(false);
                this.jPanelNoRegistro1.setVisible(true);
                return;
            }
            this.jPanelNoRegistro1.setVisible(false);
            int numeroHab = Integer.parseInt(this.jTextFieldNNumHab1.getText().trim());
            String ciudad = this.conn.rs.getString("CiudadCliente");
            java.util.Date fechaIn = this.conn.rs.getDate("fechaIn");
            java.util.Date fechaOut = this.conn.rs.getDate("fechaOut");
            int tipoHabitacion = this.conn.rs.getInt("tipoHabitacion");
            int ocupantes = this.conn.rs.getInt("ocupantes");
            int personasExtra = this.conn.rs.getInt("personasExtra");
            this.jLabelNumeroHabConfU1.setText("" + numeroHab);
            this.jLabelNombreConfU1.setText(this.conn.rs.getString("nomCliente"));
            this.jLabelCiudadConfU1.setText(ciudad);
            DateFormat formateadorFechaCompleta = DateFormat.getDateInstance(DateFormat.FULL);
            this.jLabelFechaIngConfU1.setText(formateadorFechaCompleta.format(fechaIn));
            this.jLabelFechaSalConfU1.setText(formateadorFechaCompleta.format(fechaOut));
            if (tipoHabitacion == 1) {
                this.jLabelTipoU1.setText("sencilla");
            }
            if (tipoHabitacion == 2) {
                this.jLabelTipoU1.setText("doble");
            }
            if (tipoHabitacion == 3) {
                this.jLabelTipoU1.setText("suite");
            }
            this.jLabelOcupantes1.setText("" + ocupantes);
            if (personasExtra == 1) {
                this.jCheckBoxExtraConf1.setSelected(true);
                this.jCheckBoxDosExtraConf1.setSelected(false);
            }
            if (personasExtra == 2) {
                this.jCheckBoxDosExtraConf1.setSelected(true);
                this.jCheckBoxExtraConf1.setSelected(false);
            }

            query = "SELECT piso FROM habitacion WHERE numero = " + numeroHab;
            this.conn.Consult(query);
            int piso = this.conn.rs.getInt("piso");
            this.jLabelPisoU1.setText("" + piso);
            this.jLabelLimiteT1.setText("" + tipoHabitacion);
            this.jPanelInfoHuespedByNum.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(JPanelConsultas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
