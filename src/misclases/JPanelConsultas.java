    private void consultarPorNombre() {
        this.jCheckBoxExtraConf.setSelected(false);
        this.jCheckBoxDosExtraConf.setSelected(false);
        try {
            String nombre = this.jTextFieldNombreCliente.getText().trim();
            String query = "Select * from `registro` where nomCliente = '" + nombre + "'";
            this.conn.Consult(query);
            if (this.conn.rs.getRow() == 0) {
                this.jPanelInfoHuesped.setVisible(false);
                this.jPanelNoRegistro.setVisible(true);
                return;
            }
            this.jPanelNoRegistro.setVisible(false);

            int numHabitacion = this.conn.rs.getInt("numHabitacion");
            String ciudad = this.conn.rs.getString("CiudadCliente");
            java.util.Date fechaIn = this.conn.rs.getDate("fechaIn");
            java.util.Date fechaOut = this.conn.rs.getDate("fechaOut");
            int tipoHabitacion = this.conn.rs.getInt("tipoHabitacion");
            int ocupantes = this.conn.rs.getInt("ocupantes");
            int personasExtra = this.conn.rs.getInt("personasExtra");
            this.jLabelNombreConfU.setText(nombre);
            this.jLabelCiudadConfU.setText(ciudad);
            DateFormat formateadorFechaCompleta = DateFormat.getDateInstance(DateFormat.FULL);
            this.jLabelFechaIngConfU.setText(formateadorFechaCompleta.format(fechaIn));
            this.jLabelFechaSalConfU.setText(formateadorFechaCompleta.format(fechaOut));
            this.jLabelNumeroHabConfU.setText("" + numHabitacion);
            if (tipoHabitacion == 1) {
                this.jLabelTipoU.setText("sencilla");
            }
            if (tipoHabitacion == 2) {
                this.jLabelTipoU.setText("doble");
            }
            if (tipoHabitacion == 3) {
                this.jLabelTipoU.setText("suite");
            }
            this.jLabelOcupantes.setText("" + ocupantes);
            if (personasExtra == 1) {
                this.jCheckBoxExtraConf.setSelected(true);
                this.jCheckBoxDosExtraConf.setSelected(false);
            }
            if (personasExtra == 2) {
                this.jCheckBoxDosExtraConf.setSelected(true);
                this.jCheckBoxExtraConf.setSelected(false);
            }
            this.jPanelInfoHuespedByNum.setVisible(true);
            query = "SELECT piso FROM habitacion WHERE numero = " + numHabitacion;
            this.conn.Consult(query);
            int piso = this.conn.rs.getInt("piso");
            this.jLabelPisoU.setText("" + piso);
            this.jPanelInfoHuesped.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(JPanelConsultas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
